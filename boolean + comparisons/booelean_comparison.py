# Boolean values
a = True
b = False

# Comparison operators
x = 10
y = 20

# Equal to
print(x == y)  # False

# Not equal to
print(x != y)  # True

# Greater than
print(x > y)  # False

# Less than
print(x < y)  # True

# Greater than or equal to
print(x >= y)  # False

# Less than or equal to
print(x <= y)  # True

# Logical operators
# and
print(a and b)  # False

# or
print(a or b)  # True

# not
print(not a)  # False

# 'in' comparator
list1 = [1, 2, 3, 4, 5]
print(3 in list1)  # True
print(6 in list1)  # False

# 'not in' comparator
print(3 not in list1)  # False
print(6 not in list1)  # True